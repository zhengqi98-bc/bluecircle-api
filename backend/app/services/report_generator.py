async def generate_report(
    db: AsyncSession,
    report: Report,
) -> Report:
    """Generate HTML content for a report based on its type and params."""
    report.status = "generating"

    try:
        if report.report_type == "turtle_activity":
            report.html_content = await _gen_turtle_activity(db, report)
        elif report.report_type == "fleet_overview":
            report.html_content = await _gen_fleet_overview(db, report)
        elif report.report_type == "monthly_summary":
            report.html_content = await _gen_monthly_summary(db, report)
        elif report.report_type == "alert_summary":
            report.html_content = await _gen_alert_summary(db, report)
        else:
            report.html_content = _error_html(f"Unknown report type: {report.report_type}")
            report.status = "failed"
            return report

        report.status = "completed"
        report.completed_at = datetime.now(timezone.utc)
    except Exception as e:
        report.html_content = _error_html(f"Generation failed: {str(e)}")
        report.status = "failed"

    return report


